#ifndef CH_CL_CSCON_H
#define CH_CL_CSCON_H

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <windows.h>
#include <string>
#include "EnumColor.h"

namespace ch { namespace cl {
////////////////////////////////////////////////////////////
/// \brief CsCon = Console Controller
/// 콘솔창을 컨트롤할 수 있는 함수들을 모아두었다.
////////////////////////////////////////////////////////////
class CsCon {

public:

  ////////////////////////////////////////////////////////////
  /// \brief 콘솔창의 제목을 설정한다.
  /// 사용방법 : CsCon::setConsoleTitle("제목");
  /// 
  /// \param title 콘솔 창의 제목이다.
  ////////////////////////////////////////////////////////////
  static void setConsoleTitle(const char* title);

  ////////////////////////////////////////////////////////////
  /// \brief 콘솔창의 크기를 설정한다.
  /// 크기는 콘솔창에 들어갈 수 있는 문자의 수로 정한다.
  /// 문자 하나의 크기를 보면 가로보다 세로가 더 김에 주의하자.
  /// 이 함수를 호출하면 화면의 내용을 지워버린다.
  ///
  /// \param window_x 콘솔 창에 가로로 들어가는 문자의 개수이다.
  /// \param window_y 콘솔 창에 세로로 들어가는 문자의 개수이다.
  ////////////////////////////////////////////////////////////
  static void setConsoleSize(int window_x, int window_y);

  ////////////////////////////////////////////////////////////
  /// \brief 콘솔창을 빈 화면으로 초기화한다.
  ////////////////////////////////////////////////////////////
  static void clearColsole();

  ////////////////////////////////////////////////////////////
  /// \brief 콘솔창의 배경과 글지 색을 설정한다.
  /// 사용방법 : CsCon::setConcoleColor(EnumColor::BLACK, EnumColor::RED);
  /// 
  /// \param background_color 배경의 색깔이다.
  /// \param text_color 텍스트의 색깔이다.
  ////////////////////////////////////////////////////////////
  static void setConcoleColor(EnumColor background_color, EnumColor text_color);
  ////////////////////////////////////////////////////////////
  /// \brief 주어진 시간 동안 대기한다.
  /// 
  /// \param miliseconds 대기하는 시간이다.
  /// wait(1)은 1000분의 1초 대기이다.
  ////////////////////////////////////////////////////////////
  static void sleep(unsigned long miliseconds);

  ////////////////////////////////////////////////////////////
  /// \brief 콘솔창에서 마우스가 보일지 말지 정한다.
  /// 
  /// \param is_mouse_visible true일때 마우스가 보인다. false일때 마우스가 보이지 않는다.
  ////////////////////////////////////////////////////////////
  static void setMouseVisible(bool is_mouse_visible);

private:
  int window_x_;
  int window_y_;

};  // class RenderWindow
}}  // namespace ch::cl
#endif  // CH_CL_CSCON_H


